//Crie uma função que obtenha a idade em anos e retorne a idade em dias.

//Função de inicialização do programação.
void main() {
//Função *calcAge* do tipo *int*, parâmetros *age*.
  int calcAge(age) {
//Retorna a multiplicação de *age* por 365(dias).
    return age * 365;
  }

//Recebe o valor do parâmetros da função e exibe na saída do console o retorno da mesma função.
  print(calcAge(25));
}
